package launcher.puntuaciones;

public class Puntos {
	private String nombre;
	private int puntos;
	
	public Puntos(String nombre,int puntos) {
		this.nombre=nombre;
		this.puntos=puntos;
	}

	public String getNombre() {
		return nombre;
	}

	public int getPuntos() {
		return puntos;
	}
	
	

}
